'use strict';

const fs = require('node:fs');
const path = require('node:path');

const BUILD_KEYS = ['label', 'size', 'sha256', 'support', 'board'];
const BOARD_KEYS = [
  'genericRecordWrapperVtableRva',
  'recruitingControllerVtableRva',
  'fullAddRva',
  'fullRemoveRva',
];
const MAX_UINT64 = 0xFFFFFFFFFFFFFFFFn;

function isObject(value) {
  return value !== null && typeof value === 'object' && !Array.isArray(value);
}

function requireExactKeys(value, expected, context) {
  if (!isObject(value)) {
    throw new TypeError(`${context} must be an object`);
  }

  const unknown = Object.keys(value).filter((key) => !expected.includes(key));
  if (unknown.length > 0) {
    throw new TypeError(`${context} has unknown key ${unknown[0]}`);
  }

  const missing = expected.filter((key) => !Object.hasOwn(value, key));
  if (missing.length > 0) {
    throw new TypeError(`${context} is missing ${missing[0]}`);
  }
}

function parseRva(value, key, context) {
  if (typeof value !== 'string' || !/^0x[0-9A-Fa-f]+$/.test(value)) {
    throw new TypeError(`${context}.${key} must be a hexadecimal string with a 0x prefix`);
  }

  const parsed = BigInt(value);
  if (parsed === 0n) {
    throw new RangeError(`${context}.${key} must be nonzero`);
  }
  if (parsed > MAX_UINT64) {
    throw new RangeError(`${context}.${key} must fit in an unsigned 64-bit integer`);
  }
  return parsed;
}

function parseBuild(raw, index) {
  const context = `builds[${index}]`;
  requireExactKeys(raw, BUILD_KEYS, context);

  if (typeof raw.label !== 'string' || !/^[a-z0-9]+(?:-[a-z0-9]+)*$/.test(raw.label)) {
    throw new TypeError(`${context}.label must be a nonempty lowercase kebab-case string`);
  }
  if (!Number.isSafeInteger(raw.size) || raw.size <= 0) {
    throw new TypeError(`${context}.size must be a positive safe integer`);
  }
  if (typeof raw.sha256 !== 'string' || !/^[0-9A-F]{64}$/.test(raw.sha256)) {
    throw new TypeError(`${context}.sha256 must be uppercase 64-character hexadecimal`);
  }
  if (raw.support !== 'diagnostic' && raw.support !== 'certified') {
    throw new TypeError(`${context}.support must be diagnostic or certified`);
  }

  if (raw.support === 'diagnostic') {
    if (raw.board !== null) {
      throw new TypeError(`${context} diagnostic build cannot carry a board layout`);
    }
    return { ...raw };
  }

  requireExactKeys(raw.board, BOARD_KEYS, `${context}.board`);
  const board = Object.fromEntries(BOARD_KEYS.map((key) => [
    key,
    parseRva(raw.board[key], key, `${context}.board`),
  ]));
  return { ...raw, board };
}

function parseManifest(raw) {
  requireExactKeys(raw, ['version', 'builds'], 'manifest');
  if (raw.version !== 1) {
    throw new TypeError('manifest.version must be 1');
  }
  if (!Array.isArray(raw.builds) || raw.builds.length === 0) {
    throw new TypeError('manifest.builds must be a nonempty array');
  }

  const builds = raw.builds.map(parseBuild);
  const sizes = new Set();
  const hashes = new Set();
  for (const build of builds) {
    if (sizes.has(build.size)) {
      throw new TypeError(`duplicate executable size ${build.size}`);
    }
    if (hashes.has(build.sha256)) {
      throw new TypeError(`duplicate executable sha256 ${build.sha256}`);
    }
    sizes.add(build.size);
    hashes.add(build.sha256);
  }

  return { version: 1, builds };
}

function cppRva(value) {
  return `0x${value.toString(16).toUpperCase()}ULL`;
}

function generateHeader(manifest) {
  const lines = [
    '// Generated by scripts/generate-game-builds.cjs. Do not edit.',
    '#pragma once',
    '',
    `inline constexpr std::array<GeneratedBuild, ${manifest.builds.length}> kGeneratedBuilds{{`,
  ];

  for (const build of manifest.builds) {
    lines.push(`    {"${build.label}", ${build.size}ULL,`);
    lines.push(`     "${build.sha256}",`);
    if (build.support === 'certified') {
      lines.push('     Support::kCertified,');
      lines.push(
        `     BoardLayout{${BOARD_KEYS.map((key) => cppRva(build.board[key])).join(', ')}}},`,
      );
    } else {
      lines.push('     Support::kDiagnostic, std::nullopt},');
    }
  }

  lines.push('}};', '');
  return lines.join('\n');
}

function loadManifest(manifestPath) {
  return parseManifest(JSON.parse(fs.readFileSync(manifestPath, 'utf8')));
}

function writeGeneratedHeader({ manifestPath, headerPath, check = false }) {
  const generated = generateHeader(loadManifest(manifestPath));
  if (check) {
    try {
      return fs.readFileSync(headerPath, 'utf8') === generated;
    } catch (error) {
      if (error.code === 'ENOENT') {
        return false;
      }
      throw error;
    }
  }

  fs.mkdirSync(path.dirname(headerPath), { recursive: true });
  fs.writeFileSync(headerPath, generated, 'utf8');
  return true;
}

module.exports = {
  generateHeader,
  loadManifest,
  parseManifest,
  writeGeneratedHeader,
};
